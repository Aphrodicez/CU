    # - คะแนนรวม คำนวณมาจากผลรวมของ คะแนนแต่ละส่วนคูณด้วยน้ำหนักของแต่ละส่วน
    # - คะแนนเฉลี่ย คือค่าเฉลี่ยของคะแนนรวมต่าง ๆ
    # ให้แสดงบนบรรทัดเดียวกันหมดคั่นดว้ยเครื่องหมายจุลภาคและช่องว่างหนึ่งช่อง
    # เรียงตามลำดับที่ปรากฎใน data ถ้าไม่มีใครได้ต่ำกว่าคะแนนเฉลี่ยเลย ให้แสดงคำว่า None
    mean = np.mean(np.sum(data[:, 1:], axis = 1))
    c = data[np.sum(data[:, 1:]) > mean]
